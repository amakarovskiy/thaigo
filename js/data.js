/* ===== Category helpers ===== */

function getDisplayCat(place) {
  var c = place.cat.find(function (x) { return x !== 'top' && x !== 'office'; });
  return c || place.cat[0];
}

function placeMatchesCat(place, cat) {
  if (cat === 'all') return true;
  return place.cat.includes(cat);
}

